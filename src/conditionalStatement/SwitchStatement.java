package conditionalStatement;

public class SwitchStatement {

	public static void main(String[] args) 
	{
		int x=5;
		switch(x)
		{
		case 1:
			System.out.println(" poor preformance");
			break;
		case 2:
			System.out.println(" Match the below average level");
			break;
		case 3:
			System.out.println("Meet the expection");
			break;
		case 4:
			System.out.println("Performance is good");
			break;
		case 5:
			System.out.println("Excelent performer");
			break;
			default:
				System.out.println(" you are fired");
				break;
		}
		
		
		
		

	}

}
